    private void btnDividirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDividirActionPerformed
        // TODO add your handling code here:
        Float den = Float.parseFloat(txtDen.getText());
        Float num = Float.parseFloat(txtNum.getText());
        
        Float resl = num / den;
        Float rest = num % den;
        
        float raiz = (float) Math.sqrt(num); //Usando um typecast para poder receber o valor da raiz quadrada de den.
        
        lblDiv.setText(Float.toString(resl));
        lblResto.setText(Float.toString(rest));
        lblRes.setText(Float.toString(raiz));
    }//GEN-LAST:event_btnDividirActionPerformed

